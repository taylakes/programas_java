public class ej1
{
    public ej1()
    {
        int x = 13;
        int y = 2;
        int result;
        System.out.println ("X:" +x);
        System.out.println ("Y:" +y);
        result = x + y;
        System.out.println ("x + y: "+result);
        result = x - y;
        System.out.println ("x - y: "+result);
        result = x * y;
        System.out.println ("x * y: "+result);
        result = x / y;
        System.out.println ("x / y: "+result);
        result = ++x;
        System.out.println ("Inc de x: "+result);
        result = --y;
        System.out.println ("Dec de y: "+result);
    }
}
